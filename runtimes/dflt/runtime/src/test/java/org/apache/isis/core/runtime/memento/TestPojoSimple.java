package org.apache.isis.core.runtime.memento;

import org.apache.isis.core.runtime.testsystem.TestPojo;

import edu.umd.cs.findbugs.annotations.SuppressWarnings;


public class TestPojoSimple extends TestPojo {
    private String name;
    private  TestPojoSimple object;

    public TestPojoSimple() {}

    public TestPojoSimple(final String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    @SuppressWarnings("UWF_UNWRITTEN_FIELD")
    public TestPojoSimple getObject(){
        return object;
    }


    @Override
    public int hashCode() {
        final int prime = 31;
        int result = super.hashCode();
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        result = prime * result + ((object == null) ? 0 : object.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (!super.equals(obj))
            return false;
        if (getClass() != obj.getClass())
            return false;
        TestPojoSimple other = (TestPojoSimple) obj;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        if (object == null) {
            if (other.object != null)
                return false;
        } else if (!object.equals(other.object))
            return false;
        return true;
    }


}

